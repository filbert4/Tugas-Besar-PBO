    private void jButtonEditStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditStatusActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih flight yang statusnya akan diedit.");
            return;
        }

        String idFlight = jTable1.getValueAt(selectedRow, 0).toString();
        String currentStatus = jTable1.getValueAt(selectedRow, 8).toString();
        String newStatus = JOptionPane.showInputDialog(this, "Masukkan status baru untuk flight " + idFlight + " (sebelumnya: " + currentStatus + "):", currentStatus);
        if (newStatus != null && !newStatus.trim().isEmpty()) {
            try {
                String query = "UPDATE flight SET Status = ? WHERE idFlight = ?";
                PreparedStatement pstmt = conn.prepareStatement(query);
                pstmt.setString(1, newStatus);
                pstmt.setString(2, idFlight);
                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Status flight berhasil diperbarui!");
                    loadAllFlights();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal memperbarui status flight.");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui status: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonEditStatusActionPerformed

