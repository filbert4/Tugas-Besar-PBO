    public void loadAllFlights() {
        Object[] Baris = {"idFlight", "Nomor Penerbangan", "Maskapai", "Bandara_Asal", "Bandara_Tujuan", "Tanggal", "Keberangkatan", "Tiba", "Status"};
        DefaultTableModel tabmode = new DefaultTableModel(null, Baris);
        jTable1.setModel(tabmode);
        try {
            rs = stmt.executeQuery("SELECT * FROM flight");
            while (rs.next()) {
                String a = rs.getString("idFlight");
                String b = rs.getString("Nomor_Penerbangan");
                String c = rs.getString("Maskapai");
                String d = rs.getString("Bandara_Asal");
                String e = rs.getString("Bandara_Tujuan");
                String f = rs.getString("Tanggal");
                String g = rs.getString("Keberangkatan");
                String h = rs.getString("Tiba");
                String i = rs.getString("Status");
                String[] data = {a, b, c, d, e, f, g, h, i};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data penerbangan: " + e.getMessage());
        }
    }

