    private void jButtonHapusFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusFlightActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih flight yang akan dihapus.");
            return;
        }

        String idFlight = jTable1.getValueAt(selectedRow, 0).toString();
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus flight dengan ID " + idFlight + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                String query = "DELETE FROM flight WHERE idFlight = ?";
                PreparedStatement pstmt = conn.prepareStatement(query);
                pstmt.setString(1, idFlight);
                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Flight berhasil dihapus!");
                    loadAllFlights();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus flight.");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus flight: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonHapusFlightActionPerformed

