    private void jButtonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariActionPerformed
        // TODO add your handling code here:
        String tanggal = jTextField2.getText().trim();
        String asal = jTextField3.getText().trim();
        String tujuan = jTextField4.getText().trim();

        if (tanggal.isEmpty() && asal.isEmpty() && tujuan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan setidaknya satu kriteria pencarian (Tanggal, Asal, atau Tujuan).");
            return;
        }

        StringBuilder query = new StringBuilder("SELECT * FROM flight WHERE 1=1");
        if (!tanggal.isEmpty()) {
            query.append(" AND Tanggal = '").append(tanggal).append("'");
        }
        if (!asal.isEmpty()) {
            query.append(" AND `Bandara Asal` LIKE '%").append(asal).append("%'");
        }
        if (!tujuan.isEmpty()) {
            query.append(" AND `Bandara Tujuan` LIKE '%").append(tujuan).append("%'");
        }

        Object[] Baris = {"idFlight", "Nomor Penerbangan", "Maskapai", "Bandara Asal", "Bandara Tujuan", "Tanggal", "Keberangkatan", "Tiba", "Status"};
        DefaultTableModel tabmode = new DefaultTableModel(null, Baris);
        jTable1.setModel(tabmode);

        try {
            rs = stmt.executeQuery(query.toString());
            while (rs.next()) {
                String[] data = {
                    rs.getString("idFlight"),
                    rs.getString("Nomor_Penerbangan"),
                    rs.getString("Maskapai"),
                    rs.getString("Bandara Asal"),
                    rs.getString("Bandara Tujuan"),
                    rs.getString("Tanggal"),
                    rs.getString("Keberangkatan"),
                    rs.getString("Tiba"),
                    rs.getString("Status")
                };
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mencari data penerbangan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonCariActionPerformed

