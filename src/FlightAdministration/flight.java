    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String idFlight = jTextField1.getText().trim();

        if (idFlight.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan ID Flight untuk mencari.");
            return;
        }

        Object[] Baris = {"idFlight", "Nomor Penerbangan", "Maskapai", "Bandara Asal", "Bandara Tujuan", "Tanggal", "Keberangkatan", "Tiba", "Status"};
        DefaultTableModel tabmode = new DefaultTableModel(null, Baris);
        jTable1.setModel(tabmode);

        try {
            String query = "SELECT * FROM flight WHERE idFlight = '" + idFlight + "'";
            rs = stmt.executeQuery(query);
            if (rs.next()) {
                String[] data = {
                    rs.getString("idFlight"),
                    rs.getString("Nomor_Penerbangan"),
                    rs.getString("Maskapai"),
                    rs.getString("Bandara Asal"),
                    rs.getString("Bandara Tujuan"),
                    rs.getString("Tanggal"),
                    rs.getString("Keberangkatan"),
                    rs.getString("Tiba"),
                    rs.getString("Status")
                };
                tabmode.addRow(data);
            } else {
                JOptionPane.showMessageDialog(this, "ID Flight tidak ditemukan.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mencari data penerbangan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

