    public void loadAllAirport(){
        Object[] Baris = {"idAirport", "kode_bandara", "nama_bandara", "kota", "negara"};
        DefaultTableModel tabmode = new DefaultTableModel(null, Baris);
        jTable1.setModel(tabmode);
        try {
            rs = stmt.executeQuery("SELECT * FROM airport");
            while (rs.next()) {
                String a = rs.getString("idAirport");
                String b = rs.getString("kode_bandara");
                String c = rs.getString("nama_bandara");
                String d = rs.getString("kota");
                String e = rs.getString("negara");
                String[] data = {a, b, c, d, e};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }
