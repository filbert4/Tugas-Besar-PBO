    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String namaBandara = jTextField1.getText().trim();
            if (namaBandara.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama Bandara tidak boleh kosong!");
                return;
            }
            String sql = "SELECT * FROM airport WHERE nama_bandara LIKE ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, "%" + namaBandara + "%");
            rs = pstmt.executeQuery();
            loadAllAirport();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mencari data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

