public void loadAllTickets() {
        Object[] Baris = {"ID Tiket", "Nama Penumpang", "Penerbangan", "Kelas", "Kursi", "Food", "Suite", "Tujuan", "Waktu Berangkat"};
        DefaultTableModel tabmode = new DefaultTableModel(null, Baris);
        jTable1.setModel(tabmode);
        try {
            rs = stmt.executeQuery("SELECT * FROM tickets");
            while (rs.next()) {
                String a = rs.getString("ID_Tiket");
                String b = rs.getString("Nama_Penumpang");
                String c = rs.getString("Penerbangan");
                String d = rs.getString("Kelas");
                String e = rs.getString("Kursi");
                String f = rs.getString("Food");
                String g = rs.getString("Suite");
                String h = rs.getString("Tujuan");
                String i = rs.getString("Waktu_Berangkat");               
                String[] data = {a, b, c, d, e, f, g, h, i};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading tickets: " + e.getMessage());
        }
    }
