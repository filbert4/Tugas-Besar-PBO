    public String generateTicketID(){
        String lastID = "";
        int next = 1;
        try {
            ResultSet rs = stmt.executeQuery("SELECT ID_Tiket FROM tickets ORDER BY ID_Tiket DESC LIMIT 1");
            if(rs.next()){
                lastID = rs.getString("ID_Tiket");
                next = Integer.parseInt(lastID.substring(1)) + 1;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return String.format("T%03d", next);
    }

