    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        try {
            Datum<Tiket> tiket = new Datum<>();
            String idTiket = generateTicketID();
            String namaPenumpang = PenumpangField.getText().trim();
            String kelas = KelasField.getSelectedItem().toString().trim();
            String penerbangan = PenerbanganField.getText().trim();
            String kursi = KursiField.getText().trim();
            String tujuan = TujuanField.getText().trim();
            String waktuBerangkat = WaktuBerangkatField.getText().trim();
            String food = foodField.getText().trim();

            if (namaPenumpang.isEmpty() || penerbangan.isEmpty() || kelas.isEmpty() || kursi.isEmpty() || tujuan.isEmpty() || waktuBerangkat.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
                return;
            }

            switch (kelas) {
                case "Ekonomi":
                    tiket.setIsi(new TiketEkonomi(idTiket, namaPenumpang, kelas, penerbangan, kursi, tujuan, waktuBerangkat));
                    break;
                case "Bisnis":
                    tiket.setIsi(new TiketBisnis(true, idTiket, namaPenumpang, kelas, penerbangan, kursi, tujuan, waktuBerangkat));
                    break;
                case "Eksekutif":
                    tiket.setIsi(new TiketEksekutif(true, true, idTiket, namaPenumpang, kelas, penerbangan, kursi, tujuan, waktuBerangkat));
                    break;
                default:
                    break;
            }
            String sql = "INSERT INTO tickets (ID_Tiket, Nama_Penumpang, Penerbangan, Kelas, Kursi, Food, Suite, Tujuan, Waktu_Berangkat) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, idTiket);
            pstmt.setString(2, namaPenumpang);
            pstmt.setString(3, penerbangan);
            pstmt.setString(4, kelas);
            pstmt.setString(5, kursi);
            pstmt.setString(6, null);
            pstmt.setString(7, null);
            pstmt.setString(8, tujuan);
            pstmt.setString(9, waktuBerangkat);
            if ("Bisnis".equals(kelas)) {
                pstmt.setString(6, food);
            }
            else if ("Eksekutif".equals(kelas)) {
                pstmt.setString(6, food);
                pstmt.setBoolean(7, true);
            }
            int rs = pstmt.executeUpdate();
            if (rs > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
                dispose();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

