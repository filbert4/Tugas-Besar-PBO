    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            String idAirport = jTextField1.getText().trim();
            String namaBandara = jTextField2.getText().trim();
            String kota = jTextField3.getText().trim();
            String negara = jTextField4.getText().trim();

            if (idAirport.isEmpty() || namaBandara.isEmpty() || kota.isEmpty() || negara.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
                return;
            }

            String sql = "UPDATE airport SET nama_bandara = ?, kota = ?, negara = ? WHERE idAirport = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, namaBandara);
            pstmt.setString(2, kota);
            pstmt.setString(3, negara);
            pstmt.setString(4, idAirport);
            int rs = pstmt.executeUpdate();
            if (rs > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan!");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

